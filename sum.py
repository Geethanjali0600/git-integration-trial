def calculate_sum(a, b):
    return a + b

def calculate_product(a, b):
    return a * b

def main():
    num1 = 10
    num2 = "5"  
    sum_result = calculate_sum(num1, num2)  
    product_result = calculate_product(num1, num2)  
    print("Sum:", sum_result)
    print("Product:", product_result)

main()
